function emplFilter(employeesInfo, criteria) {
    let employeesData = JSON.parse(employeesInfo);
    const [criteriaKey, criteriaValue] = criteria.split('-');

    if (criteriaValue !== 'all') {
        employeesData = employeesData.filter(a => a[criteriaKey] == criteriaValue);
    }

    employeesData.forEach((employee, index) => console.log(`${index}. ${employee['first_name']} ${employee['last_name']} - ${employee.email}`));
}

